export function getUserName(usersObject){
    let claves = Object.keys(usersObject);
    let numRandom = Math.floor(Math.random() * claves.length);
    let nombre = claves[numRandom];
    let info = usersObject[claves[numRandom]]

    return {nombre , info};
}

export function getUserInfo(userSelected){
    let valores = Object.values(userSelected);
    return valores
}